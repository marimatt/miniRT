char	*ft_strrchr(const char *s, int c)
{
	char	k;
	int		i;
	char	*res;

	res = (char *)s;
	i = 0;
	k = (char)c;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == k)
			return (&res[i]);
		i--;
	}
	return (0);
}
